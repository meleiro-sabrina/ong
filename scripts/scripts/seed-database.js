function randomItem(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}
function randomInt(min, max) {
    return Math.floor(Math.random() * (max - min + 1)) + min;
}
function formatPhoneNumber() {
    const ddd = randomInt(11, 99);
    const first = randomInt(90000, 99999);
    const second = randomInt(1000, 9999);
    return `(${ddd}) ${first}-${second}`;
}
function formatDate() {
    const day = String(randomInt(1, 28)).padStart(2, '0');
    const month = String(randomInt(1, 12)).padStart(2, '0');
    const year = randomInt(1990, 2018);
    return `${day}/${month}/${year}`;
}
function formatCEP() {
    const cep = randomInt(1000000, 9999999);
    return `0${String(cep).padStart(7, '0').slice(0, 5)}-${String(cep).padStart(7, '0').slice(5)}`;
}
function generateName() {
    return `${randomItem(firstNames)} ${randomItem(lastNames)} ${randomItem(lastNames)}`;
}
function generateEmail(name) {
    const cleanName = name.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '').replace(/\s+/g, '.');
    const domains = ['gmail.com', 'yahoo.com.br', 'hotmail.com', 'outlook.com', 'uol.com.br', 'bol.com.br', 'terra.com.br', 'ig.com.br'];
    return `${cleanName}.${randomInt(1, 999)}@${randomItem(domains)}`;
}
